package idat.edu.pe.dto;

public class UsuarioResponseDTO {

	private Integer idusuarioRp;
	private String usuarioRp;
	private String passwordRp;
	private String rolRp;
	
	public Integer getIdusuarioRp() {
		return idusuarioRp;
	}
	public void setIdusuarioRp(Integer idusuarioRp) {
		this.idusuarioRp = idusuarioRp;
	}
	public String getUsuarioRp() {
		return usuarioRp;
	}
	public void setUsuarioRp(String usuarioRp) {
		this.usuarioRp = usuarioRp;
	}
	public String getPasswordRp() {
		return passwordRp;
	}
	public void setPasswordRp(String passwordRp) {
		this.passwordRp = passwordRp;
	}
	public String getRolRp() {
		return rolRp;
	}
	public void setRolRp(String rolRp) {
		this.rolRp = rolRp;
	}
	
	
	
}
