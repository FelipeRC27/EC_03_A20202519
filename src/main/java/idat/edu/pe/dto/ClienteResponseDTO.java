public class ClienteResponseDTO {

	private Integer idclienteRp;
	private String nombreRp;
	private String celularRp;
	
	
	public Integer getIdclienteRp() {
		return idclienteRp;
	}
	public void setIdclienteRp(Integer idclienteRp) {
		this.idclienteRp = idclienteRp;
	}
	public String getNombreRp() {
		return nombreRp;
	}
	public void setNombreRp(String nombreRp) {
		this.nombreRp = nombreRp;
	}
	public String getCelularRp() {
		return celularRp;
	}
	public void setCelularRp(String celularRp) {
		this.celularRp = celularRp;
	}
	
	
	
}
