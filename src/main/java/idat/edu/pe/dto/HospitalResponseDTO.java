package idat.edu.pe.dto;

public class HospitalResponseDTO {

	private Integer idhospitalRp;
	private String nombreRp;
	private String descripcionRp;
	private String distritoRp;
	
	
	public Integer getIdhospitalRp() {
		return idhospitalRp;
	}
	public void setIdhospitalRp(Integer idhospitalRp) {
		this.idhospitalRp = idhospitalRp;
	}
	public String getNombreRp() {
		return nombreRp;
	}
	public void setNombreRp(String nombreRp) {
		this.nombreRp = nombreRp;
	}
	public String getDescripcionRp() {
		return descripcionRp;
	}
	public void setDescripcionRp(String descripcionRp) {
		this.descripcionRp = descripcionRp;
	}
	public String getDistritoRp() {
		return distritoRp;
	}
	public void setDistritoRp(String distritoRp) {
		this.distritoRp = distritoRp;
	}
	
	
	
}
